long long f(long long n){
    long long ans=0;
    if(n>=10){
        n/=10;
        ans+=n;
        while(n>=5){
            n/=5;
            ans+=n;
        }
    }
    return ans;
}
int main(){
   long long n;
   cin >> n;
   if(n%2==1) cout << 0 << endl;
   else{
       cout << f(n) << endl;
   }
}