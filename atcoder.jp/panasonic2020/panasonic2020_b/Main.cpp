int main(){
   unsigned long long h,w,a,b,ans;
   cin >> h >> w;
   if(h==1 || w==1) ans=1;
   else{
      a=(w+1)/2;
      b=(h+1)/2;
      ans=a*b+(h-b)*(w-a);
   }
   cout << ans << endl;
}